package main;

public class StitchedPictureSize {

	private int widthOfOutputImage;
	private int heightOfOutputImage;
	private int shiftRight;
	private int shiftDown;
	
	public int getWidthOfOutputImage() {
		return widthOfOutputImage;
	}
	public void setWidthOfOutputImage(int widthOfOutputImage) {
		this.widthOfOutputImage = widthOfOutputImage;
	}
	public int getHeightOfOutputImage() {
		return heightOfOutputImage;
	}
	public void setHeightOfOutputImage(int heightOfOutputImage) {
		this.heightOfOutputImage = heightOfOutputImage;
	}
	public int getShiftRight() {
		return shiftRight;
	}
	public void setShiftRight(int shiftRight) {
		this.shiftRight = shiftRight;
	}
	public int getShiftDown() {
		return shiftDown;
	}
	public void setShiftDown(int shiftDown) {
		this.shiftDown = shiftDown;
	}
	
}
